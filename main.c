/**
  * @brief  Rx Transfer completed callback.
  * @param  huart: Pointer to a UART_HandleTypeDef structure that contains
  *                the configuration information for the specified UART module.
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  static uint8_t channel_index = 0;
  static uint8_t start_code_received = 0;
  
  if(huart->Instance == USART1) {
    // Check for break condition (start code 0x00)
    if(dmx_data[0] == DMX_START_CODE && !start_code_received) {
      start_code_received = 1;
      channel_index = 0;
      // Start receiving DMX data
      HAL_UART_Receive_IT(&huart1, &dmx_data[channel_index], 1);
    }
    else if(start_code_received) {
      // Store DMX channel data
      dmx_data[channel_index] = dmx_data[0];
      channel_index++;
      
      if(channel_index < DMX_CHANNELS) {
        // Continue receiving next channel
        HAL_UART_Receive_IT(&huart1, &dmx_data[channel_index], 1);
      } else {
        // All channels received
        dmx_received = 1;
        start_code_received = 0;
        channel_index = 0;
        // Start listening for next frame
        HAL_UART_Receive_IT(&huart1, &dmx_data[0], 1);
      }
    } else {
      // Continue listening for start code
      HAL_UART_Receive_IT(&huart1, &dmx_data[0], 1);
    }
  }
}

